class Program3 {
    public static void main(String[] args) {
        int x = 10, y = 20;

        // the logcal operator in Java yield boolean value, either true or false 

        System.out.println(x>y);
        System.out.println(x<y);
        System.out.println(x<=y);
        System.out.println(x>=y);
        System.out.println(x!=y);
        System.out.println(x==y);
        
    }
}
